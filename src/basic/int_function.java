package basic;

public class int_function {  

	public static void main(String[] args) {
		
		
		int a=25;
		int b=39;
		
		if (a==b)
			System.out.printf("%d==%d\n",a,b);
		if (a<b)
			System.out.printf("%d<%d\n",a,b);
		if (a<=b)
			System.out.printf("%d<=%d\n",a,b);
		if (a>=b)
			System.out.printf("%d>=%d\n",a,b);
		if (a!=b)
			System.out.printf("%d!=%d\n",a,b);
		
		double c=25.1;
		double d=39.2;
		
		if (a==b)
			System.out.printf("%f==%f\n",c,d);
		if (a<b)
			System.out.printf("%f<%f\n",c,d);
		if (a<=b)
			System.out.printf("%f<=%f\n",c,d);
		if (a>=b)
			System.out.printf("%f>=%f\n",c,d);
		if (a!=b)
			System.out.printf("%f!=%f\n",c,d);
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}
}
